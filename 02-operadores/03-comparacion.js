let a = 10;

// Relacionales
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

// Igualdad - NO USAR MUCHO ESTOS SIMBOLOS
console.log(a == 10);
console.log(a =! 10);
// Solo pregunta si los valores son iguales, pero no los tipos
console.log(a == '10');
// Consulta el valor y el tipo de dato que se compara
console.log(a === '10');
console.log(a !== '10');